public class SivitasAkademik {
    private String id;
    private String nama;

    // Konstruktor yang memerlukan dua parameter: id dan nama
    public SivitasAkademik(String id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    // Getter dan setter
    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }
}
